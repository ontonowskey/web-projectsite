    <br>
    Важным этапом в развитии международных и межсоюзнических отношений стала Тегеранская конференция 1943. В ходе её проведения с нашими основными союзниками США и Великобританией была достигнута договорённость об открытии второго фронта в мае 1944 г. СССР заявил о готовности выполнить свои союзнические обязательства и вступить в войну с Японией после разгрома Германии.<br><br>

    Антигитлеровская коалиция возросла с 26 до 33 государств.<br><br>

    Несмотря на крупные военные успехи и изменение общей стратегической обстановки на советско-германском фронте во втором периоде войны, враг был ещё силён. Под оккупацией оставалась значительная часть территории СССР.


