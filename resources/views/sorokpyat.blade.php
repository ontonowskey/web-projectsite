    Накануне Дня Победы, англичане и американцы, опасаясь конфликта с Советским Союзом при встрече войск союзников, разработали операцию «Немыслимое», которая предусматривала сразу два варианта развития событий – как наступление на русских, так и оборону. Однако сила и стремительность, проявленные советскими войсками во время Берлинской операции и непосредственно Штурма Берлина, вынудили западных союзников отказаться от этой идеи. Тем не менее, сам факт разработки операции «Немыслимое» уже стал предпосылкой Холодной войны. Между тем, 25 апреля произошла встреча на Эльбе русских и американцев в крайне дружелюбной и радостной атмосфере.<br><br>

    В ночь с 8 на 9 мая (9 мая – по московскому времени, по центральноевропейскому было еще 8 мая) верховный главнокомандующий Вермахта Вильгельм Кейтель подписал Акт о безоговорочной капитуляции Германии, и Великая Отечественная война подошла к концу (но не Вторая мировая война), а 9 мая стало общегосударственным праздником – Днем Победы.<br><br>

    Забавный факт – во время подписания акта о капитуляции, командующий Вермахта Вильгельм Кейтель, когда вручил Жукову документ о капитуляции Германии, увидел представителей Франции. Фельдмаршал не удержался и спросил: «А что, эти тоже нас победили?».

