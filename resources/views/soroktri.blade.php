<div class="soderjanie">
    Ко второй половине ноября 1942 г. положение СССР оставалось <strong>тяжёлым</strong>. Враг стоял у Воронежа, Сталинграда и предгорий Кавказа, захватил важнейшие экономические районы страны, находился <strong>в 150–200 км от Москвы</strong>, блокировал Ленинград. Протяжённость фронта достигла 6200 км. Второй фронт не был открыт, что позволило немецкому командованию перебросить на Восток в течение лета и осени 1942 г. <strong>около 80 дивизий</strong>. <br>
    Одним из исторических этапов на пути к победе Советского Союза над фашистской Германией явилась <strong>Сталинградская битва</strong> 1942–43 гг., победа в которой положила начало коренному перелому в ходе Великой Отечественной и всей Второй мировой войны. Стратегическая инициатива перешла к советским ВС. Эта победа имела огромное международное значение: благодаря ей усилилась борьба народов Европы против оккупантов, Турция и Япония отказались от первоначального намерения выступить против СССР, укрепилась антигитлеровская коалиция.
