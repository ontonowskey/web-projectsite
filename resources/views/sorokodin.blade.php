    <div class="soderjanie1">
        22 июня – Начало Великой Отечественной войны. <br>
        30 июня – Создание Государственного Комитета Обороны <br>
        3 июля Обращение председателя СНК и ГКО СССР И.В. Сталина к советскому народу: «Все для фронта, все для победы»<br>
        10 июля – 10 сентября – Смоленское сражение.<br>
        7 августа – И.В. Сталин занял пост Верховного Главнокомандующего вооруженными силами СССР.<br>
        8 сентября – Прорыв немецких войск к Ладожскому озеру и захват ими Шлиссельбурга. Начало обороны Ленинграда. <br>
        30 сентября 1941 года – 20 апреля 1942 года – Битва под Москвой.<br>
        15 октября Государственный Комитет обороны принял решение об эвакуации из Москвы части правительственных учреждений.<br>
        30 октября 1941 года – 4 июля 1942 года – Оборона Севастополя.<br>
        7 ноября – США распространили действие закона о ленд-лизе на Советский Союз.<br>
        15 ноября – 5 декабря – Клинско-Солнечногорская оборонительная операция войск Западного фронта.<br>
        17 ноября – 2 декабря – Контрнаступление войск Южного фронта под Ростовом-на-Дону.<br>
        22 ноября – Начало работы ледовой трассы на Ладожском озере.<br>
        5 декабря 1941 года – 7 января 1942 года – Контрнаступления советских войск под Москвой.
