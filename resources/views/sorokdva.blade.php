    Основные события
</div>
<div class="soderjanie1">
    1 января 26 государств, в том числе США, СССР, Великобритания и Китай, подписали в Вашингтоне декларацию Объединенных Наций.<br>
    12–29 мая – Харьковское сражение. Поражение советских войск.<br>
    20 мая – Учреждение ордена Отечественной войны I и II степеней.<br>
    26 мая Договор между Советским Союзом и Великобританией о союзе в войне против гитлеровской Германии и ее сообщников в Европе и о сотрудничестве и взаимной помощи после войны.<br>
    Ноябрь Наибольшее продвижение войск Германии и ее союзников на советско-германском фронте: до Воронежа, Сталинграда, Новороссийска, Майкопа и предгорий Кавказа.<br>
    19 ноября 1942 года – 2 февраля 1943 года – Наступательный период Сталинградской битвы.<br><br>
    События и причины поражений весны и лета 1942 г. в точности повторили неудачи 1941 г. В их основе лежали ошибки, допущенные Сталиным и высшим командным составом. Сказалось отсутствие опыта в планировании операций и недооценка сил противника.<br><br>
    Страна оказалась в тяжелом положении, а выправлять ситуацию пришлось простым солдатам.<br>
    28 июля 1942 г. был отдан приказ Верховного Главнокомандующего №227: «Ни шагу назад!» Вводились жестокие наказания за отступление без приказа свыше. Позади войск стали располагать заградительные части НКВД.
