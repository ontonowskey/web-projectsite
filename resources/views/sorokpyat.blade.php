<div class="soderjanie1">
    12 января 1945 года ознаменовалось началом Висло-Одерской операции (между реками Висла и Одер), которая стала одной из самых стремительных военных операций XX века. Менее чем за месяц, к 3 февраля, советские солдаты совместно с восставшими поляками практически полностью освободили Польшу и заняли Восточную Пруссию. За время операции немцы потеряли до 800 тысяч солдат Вермахта, а также множество техники и вооружений.<br><br>

    В ходе Восточно-Померанской операции (февраль-март 1945 года) были освобождены остатки северных территорий Польши и занята Восточная Померания (северо-восток Германии).<br><br>

    С 4 по 11 февраля 1945 года в освобожденном Крыму прошла Ялтинская конференция в Ливадийском дворце. Эта встреча стала продолжением Тегеранской конференции лидеров антигитлеровской коалиции – Черчилля, Рузвельта и Сталина. Главы государств собрались для решения вопроса о послевоенном мировом устройстве (все уже понимали, что поражение Германии – дело времени). Кроме вопроса о новых границах европейских стран, был рассмотрен и вопрос некоторых восточноазиатских территорий после поражения Японии (именно тогда было решено, что Курильские острова и Южный Сахалин снова переходили России). Не менее важными стали переговоры по созданию новой международной организации взамен Лиги Наций. Так были созданы предпосылки создания ООН.<br><br>

    В феврале-мае 1945 года прошло три завершающих операции Великой Отечественной войны:<br></div>
<div class="text">
    Будапештская операция (началась еще в конце октября 1944 года и закончилась 13 февраля 1945 года полным освобождением Венгрии и ее столицы Будапешта, а также выходом на венское направление).
    Венская операция (с 16 марта по 15 апреля 1945 года, - освобождение от фашистов Австрии и ее столицы – Вены).
    Берлинская операция (16 апреля – 8 мая, - захват Восточной Германии, битва за Берлин и последующее его взятие, полная ликвидация немецкого правительства и победа антигитлеровской коалиции).<br><br>
</div>
